# Write a Python program that replace all occurrences of the first word with the second word and print the result.

# Step 1: The sentence
sentence = "Python ID - Online Python Compiler"

# Step 2: The word to replace and the word to replace it with
word_to_replace = "Online"
replacement_word = "Helpful"

# Step 3: Replace all occurrences of the word
updated_sentence = sentence.replace(word_to_replace, replacement_word)

# Step 4: Display the updated sentence
print(f"Updated sentence: {updated_sentence}")